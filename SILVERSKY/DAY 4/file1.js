//Datatypes in JavaScript
let num = 42; // Number
let str = "Hello, World!"; // String
let bool = true; // Boolean
let arr = [1, 2, 3, 4, 5]; // Object (Arrays are a type of object in JavaScript)
let obj = { name: "Alice", age: 30 }; // Object
let undef; // Undefined
let nul = null;
console.log(num+" "+typeof num); // "number"
console.log(str+" "+typeof str); // "string"
console.log(bool+" "+typeof bool); // "boolean"     
console.log(arr+" "+typeof arr); // "object"
console.log(obj+" "+typeof obj); // "object"
console.log(undef+" "+typeof undef); // "undefined"
console.log(nul+" "+ typeof nul); // "object" (this is a quirk in JavaScript)
